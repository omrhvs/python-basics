# Conditional Statements - IF
edad = 18

if edad >= 18:
    print ("Eres mayor de edad.")

# IF-ELSE
if edad >= 18:
    print ("Eres mayor de edad.")
else:
    print ("Eres menor de edad.")

# IF-ELIF-ELSE
calificacion = 85

if calificacion >= 90:
    print ("Excelente")

elif calificacion >= 80:
    print ("Muy bueno")

elif calificacion >= 70:
    print ("Bueno")

else:
    print ("Necesita mejorar")